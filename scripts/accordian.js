$("#community-discussion-button").click(function() {
    if ($("#community-discussion-text").hasClass("accessible-hidden")) {
        $("#community-discussion-text").removeClass("accessible-hidden");
    } else {
        $("#community-discussion-text").addClass("accessible-hidden");
    }
});
